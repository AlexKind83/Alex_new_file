
# Изменяем строку с использованием дополнительной переменной
str1 = 'JavaScript'
s = str1.replace('JavaScript', 'Python')
print(s)


# Изменение строки применяя метод к самой строке
str2 = 'JavaScript'.replace('JavaScript', 'Python')
print(str2)


# Изменение строки применяя метод к строке внутри print
str3 = 'JavaScript'
print(str3.replace('JavaScript', 'Python'))
